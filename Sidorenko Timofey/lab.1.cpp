#include <math.h>
#include <stdio.h>

int main()
{
	system("chcp 1251");
	long double number; long int temp;
	printf("Введите вещественное число ","/n");
	do {
		temp = (int)(scanf_s("%lf", &number));
		while (getchar() != '\n');
		if (temp == 1) break;
		else printf("%s", "Некорректный ввод\n");
	} while (temp != 1);
	int rubl = (int)number;
	int kop = (int)((number - rubl)*1000);
	if (kop % 10 >= 6 && kop % 10 <= 9) {
		kop = kop / 10 + 1;
	}
	else {

		kop = kop / 10;
	}
	printf("%dруб. %d коп. ",rubl,kop);
	return 0;
}

